    //fetch customer table
    private void refreshCustomersTable() {
        List<MenuItem> menuItems = menuDAO.getAll();
        DefaultTableModel model = (DefaultTableModel) jTableCustomer.getModel();
        //Clear all items in tblContacts
        for(int i = model.getRowCount() - 1; i >= 0; i-- ) {
            model.removeRow(i);
        }
        for (MenuItem menuItem : menuItems) {
                Object[] row = new Object[4];
                row[0] = menuItem.getID();
                row[1] = menuItem.getName();
                row[2] = menuItem.getCategory();
                row[3] = menuItem.getUnitPrice();
                model.addRow(row);
        }
    }

    //fetch order table
    private void refreshOrdersTable() {
        List<Order> orders = orderDAO.getAll();
        DefaultTableModel model = (DefaultTableModel) jTableOrder.getModel();
        //Clear all items in tblContacts
        for(int i = model.getRowCount() - 1; i >= 0; i-- ) {
            model.removeRow(i);
        }
        for (Order order : orders) {
                Object[] row = new Object[6];
                row[0] = order.getID();
                row[1] = order.getItemName();
                row[2] = order.getOptions();
                row[3] = order.getQuantity();
                row[4] = order.getUnitPrice();
                row[5] = order.getTotal();
                model.addRow(row);
        }
    }

